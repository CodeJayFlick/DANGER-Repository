class EntropyRecord:
    def __init__(self, name: str, center: float, width: float):
        self.name = name
        self.center = center
        self.width = width


# Example usage:
record1 = EntropyRecord("Example 1", 0.5, 2.3)
print(record1.name)  # Output: "Example 1"
print(record1.center)  # Output: 0.5
print(record1.width)   # Output: 2.3
