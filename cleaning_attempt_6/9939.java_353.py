class WindowPosition:
    TOP = 1
    BOTTOM = 2
    LEFT = 3
    RIGHT = 4
    
    WINDOW = "WINDOW"
    STACK = "STACK"

print(WindowPosition.TOP)
print(WindowPosition.BOTTOM)
print(WindowPosition.LEFT)
print(WindowPosition.RIGHT)
print(WindowPosition.WINDOW)
print(WindowPosition.STACK)
